#include <iostream.h>

int main() {
    int integerVar = 42;              // Integer
    float floatVar = 3.14f;           // Float
    double doubleVar = 3.14159265;    // Double
    char charVar = 'C';               // Character
    bool boolVar = true;              // Boolean
    short shortVar = 32000;           // Short Integer
    long longVar = 1234567890;        // Long Integer
    unsigned int unsignedIntVar = 100; // Unsigned Integer

    cout << "Integer: " << integerVar << endl;
    cout << "Float: " << floatVar << endl;
    cout << "Double: " << doubleVar << endl;
    cout << "Char: " << charVar << endl;
    cout << "Boolean: " << boolVar << endl;
    cout << "Short: " << shortVar << endl;
    cout << "Long: " << longVar << endl;
    cout << "Unsigned Int: " << unsignedIntVar << endl;

    return 0;
}
