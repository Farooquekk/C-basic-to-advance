#include <iostream>
using namespace std;

int main() {
    int integerVar = 10;
    float floatVar = 3.14f;
    double doubleVar = 3.14159;
    char charVar = 'A';
    bool boolVar = true;

    cout << "Integer: " << integerVar << endl;
    cout << "Float: " << floatVar << endl;
    cout << "Double: " << doubleVar << endl;
    cout << "Char: " << charVar << endl;
    cout << "Boolean: " << boolVar << endl;

    return 0;
}
