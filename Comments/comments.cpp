#include <iostream.h>

int main() {
    // This is a single-line comment
    int age = 25;  // Declare and initialize age

    /* 
       This is a multi-line comment.
       It can span multiple lines.
    */
    float height = 5.9;

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;

    return 0;
}
